    /*Afficher les elements de la table souhaité*/
     private void Table(){
         model.setRowCount(0);
         
         
         try {
             conn.seConnecter();
             stmt = conn.getStmt();
             rs = stmt.executeQuery("SELECT Enfant.id AS enfantId, nom, prenom, hendicape, heureArr, heureDep\n" +
                                    "FROM Enfant\n" +
                                    "INNER JOIN CompteParent ON CompteParent.id = Enfant.compteParent_id\n" +
                                    "WHERE isConnected = 1 ;");
             
             while(rs.next()){
        model.addRow(new Object[]{rs.getString("enfantId"), rs.getString("nom"), rs.getString("prenom"), rs.getString("hendicape"),rs.getString("heureArr"),rs.getString("heureDep") }  );
        
        }
         } catch (SQLException e) {
             JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
         }
         tableDeAccueil.setModel(model);
         conn.closeConnexion();
     }
     
     
