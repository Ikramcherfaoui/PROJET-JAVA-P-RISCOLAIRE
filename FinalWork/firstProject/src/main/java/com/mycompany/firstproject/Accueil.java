    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String nom = nomE.getText();
            String prenom = prenomE.getText();
            int age = Integer.valueOf(ageE.getText());
            String handicapE = handicap.getText();
            String heurreArrive = heureAr.getSelectedItem().toString();
            String heurreDep = heurDe.getSelectedItem().toString();
            
            Enfant enf = new Enfant(nom,prenom,age,handicapE,heurreArrive,heurreDep);
            
            String query = "INSERT INTO Enfant(nom,prenom,age,hendicape,heureArr,heureDep,compteParent_id)\n" +
                           "SELECT '"+enf.getNom()+"', '"+enf.getPrenom()+"', "+enf.getAge()+" , '"+enf.getHandicap()+"', '"+enf.getHeureArr()+"', '"+enf.getHeureDep()+"', id \n" +
                           "FROM CompteParent\n" +
                           "WHERE isConnected = 1;";
            
            String query2 = "SELECT  nom, prenom\n" +
                            "FROM Enfant\n" +
                            "INNER JOIN CompteParent ON CompteParent.id = Enfant.compteParent_id\n" +
                            "WHERE isConnected = 1 ;";
            
            Boolean existe = false;
            
            try {
            conn.seConnecter();
            stmt = conn.getStmt();
            
            rs = stmt.executeQuery(query2);
            if(rs.next()){
               while(rs.next()){
                   if(nom.equals(rs.getString("nom"))&& prenom.equals(rs.getString("prenom"))){
                     existe = true ;
                     
                   }
               }
           
            }
            
            if(!existe){
              stmt.executeUpdate(query);
            
              nomE.setText("");
              prenomE.setText("");
              ageE.setText("");
              handicap.setText("");
            }else{
              JOptionPane.showMessageDialog(null, "Cet enfant existe déja");
            }
            
        } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }
            conn.closeConnexion();
            Table();
            
    }//GEN-LAST:event_jButton1ActionPerformed

